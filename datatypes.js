let x = 7;
console.log(typeof x);
// here the datatype of a digit is number

let y = true;
console.log(typeof y);
//here the datatype of true or false is boolean

let z = 'francischima';
console.log(typeof z);
//here the datatype of a letter is called string.

//  Also note that we write a string inside a single or double 
// quote.  
// A number written inside a quote will be treated as a string

let a = '6';
console.log(typeof a);
// here variable a is treated as a string

let b;
console.log(b);
console.log(typeof b);
// here a variable without a value will have a datatype of undefined



// note that datatypes are for the values not for variables